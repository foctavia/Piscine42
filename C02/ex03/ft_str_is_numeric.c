int	ft_str_is_numeric(char *str)
{
	int	n;
	int	i;

	i = 0;
	n = 1;
	while ((str[i] != '\0') && (n != 0))
	{
		if (str[i] < '0' || str[i] > '9')
			n = 0;
		else
			i++;
	}
	return (n);
}
