int	ft_str_is_uppercase(char *str)
{
	int	n;
	int	i;

	i = 0;
	n = 1;
	while ((str[i] != '\0') && (n != 0))
	{
		if ((str[i] < 'A') || (str[i] > 'Z'))
			n = 0;
		else
			i++;
	}
	return (n);
}
